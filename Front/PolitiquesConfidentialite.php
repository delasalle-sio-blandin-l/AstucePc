    <h2 class="sous-titre">Art.1 Règles de confidentialité</h2>

    <p>Le règlement général sur la protection des données du 27 avril 2016 (ci-après le RGPD) est d’application à partir du 
        25 mai 2018. Il impose des règles et conditions strictes aux entreprises et commerçants dans le cadre du traitement 
        des données à caractère personnel de leurs clients et prospects, et ce afin de protéger la vie privée de ceux-ci.
    </p>

    <h2 class="sous-titre">Art.2 Le responsable du traitement</h2>

    <p>Le « responsable du traitement » de vos données à caractère personnel est Astuce PC, responsable du site que vous 
        consultez et auquel vous communiquez des données.
    </p>

    <h2 class="sous-titre">Art.3 Base légales du traitement des données et utilisation</h2>

    <p>Nous ne pouvons utiliser vos données à caractère personnel qu’à des fins à la fois légitimes et nécessaires 
        (art. 6 du RGPD). Cela signifie concrètement que nous traitons vos données à caractère personnel, que cela soit ou 
        non sous forme électronique, pour des finalités légitimes dans le cadre de la relation contractuelle, du business et
        de la sécurité/sûreté.
    </p>

    <p>Ces finalités sont notamment, mais pas seulement, les suivantes :</p>

    <ul>
        <li>Traiter et exécuter vos commandes et contrats ;</li>
        <li>Gérer votre compte et la relation commerciale ;</li>
        <li>Vous communiquer des informations commerciales, promotionnelles, publicitaires ou sur l’utilisation de nos 
            services ;
        </li>
        <li>Communications dans le cadre de l’exécution d’un contrat ;</li>
        <li>Améliorer la conception, la mise en page, et la fonctionnalité globale du site ;</li>
        <li>Analyser les données à des fins statistiques</li>
    </ul>

    <h2 class="sous-titre">Art.4 Qu’est-ce qu’une donnée à caractère personnel</h2>

    <p>Les données à caractère personnel incluent toutes les informations qui vous concernent et sur base desquelles vous 
        pouvez être identifié (ci-après dénommées « les données »). Les données anonymes, qui ne permettent pas de vous 
        identifier, ne sont par conséquent pas considérées comme des données à caractère personnel. Vos données à caractère 
        personnel peuvent donc comprendre :
    </p>

    <ul>
        <li>Données relatives à votre identité (nom, prénom, adresse, numéro à la TVA, numéro d’entreprise, etc.) ;</li>
        <li>Données de statut personnel (numéro de téléphone, e-mail personnel, …) ;</li>
        <li>Données financières et sur les transactions (Données relatives au moyen de paiement, numéro de compte 
            bancaire, détails de facturation, …) ;
        </li>
        <li>Données relatives à l’exécution du contrat conclu avec nous (objet du contrat, adresse de facturation, données 
            professionnelles, …) ;
        </li>
        <li>Données relatives à l’utilisation d’équipement électronique, tel que des ordinateurs (mot de passe, log 
            data, données d’identification électronique, détails de facturation, …) ;
        </li>
    </ul>

    <p>données sensibles : (en plus petit)</p>

    <p>Le traitement de données à caractère personnel révélant l’origine raciale ou ethnique, les opinions politiques, les 
        convictions religieuses ou philosophiques, ainsi que le traitement des données génétiques, des données biométriques 
        aux fins d’identifier une personne physique de manière unique, des données concernant la vie sexuelle ou 
        l’orientation sexuelle d’une personne physique sont interdits.
    </p>

    <p>Nous nous engageons à respecter strictement cette interdiction.</p>

    <h2 class="sous-titre">Art.5 Sources et origines des données à caractère personnel</h2>

    <p>En principe, les données que nous possédons proviennent de vous, lorsque vous remplissez un formulaire. Si vous 
        n’entendez pas fournir les informations obligatoires ou nécessaires, vous pouvez perdre le bénéfice de certains 
        avantages et/ou nous pouvons décider de mettre fin à nos services à votre profit ou nous serons dans l’impossibilité 
        d’exécuter le contrat.
    </p>

    <h2 class="sous-titre">Art.6 Accès aux données à caractère personnel – Avec qui partageons nous vos informations ?</h2>

    <p>Conformément à la réglementation en vigueur, les données peuvent être transmises aux autorités compétentes sur requête 
        et notamment aux organismes publics, exclusivement pour répondre aux obligations légales, les auxiliaires de 
        justice, les officiers ministériels et les organismes chargés d’effectuer le recouvrement de créances, ainsi que dans 
        le cas de la recherche des auteurs d’infractions commises sur internet.
    </p>

    <p>Vos données sont essentiellement à usage interne. Nous pouvons communiquer vos données à des entreprises tierces, qui 
        seront en mesure de les traiter, dans le cadre de motifs légitimes et notamment de la bonne exécution du 
        contrat, lorsque nous faisons appel à des prestataires et des sous-traitants pour la réalisation de la commande ou de 
        services fournis par nous (prestations techniques, service de paiement, sécurisation du paiement, livraisons, envoi 
        de prospection commerciale …).
    </p>

    <p>Nous leur transmettons uniquement les données dont ils ont besoin pour effectuer leurs services.</p>

    <p>Nous veillerons toutefois au respect de la réglementation RGPD par nos sous-traitants.</p>

    <p>Le traitement des données par ces derniers est régi par un cadre légal strict.</p>

    <h2 class="sous-titre">Art.7 Durée de conservation des données</h2>

    <p>Nous mettons les moyens nécessaires en œuvre afin d’assurer que la conservation des données à caractère personnel pour 
        les finalités décrites ci-dessus n’outrepasse pas les durées légales.
    </p>

    <h2 class="sous-titre">Art.8 Quels sont vos droits ?</h2>

    <p>Nous nous engageons à prendre les mesures techniques et organisationnelles appropriées afin de garantir la sécurité du 
        traitement des données à caractère personnel de chacun (Art. 32 du RGPD).
    </p>

    <p>Conformément aux lois et règlements applicables en matière de protection des données personnelles, vous bénéficiez 
        d’un certain nombre de droits relatifs à vos données personnelles à savoir :
    </p>

    <ul>
        <li>- Droit d’accès (Art. 15 du RGPD) et d’information : Vous avez le droit d’être informé de manière 
            concise, transparente, intelligible et facilement accessible de la manière dont vos données sont traitées. Vous 
            avez également le droit d’obtenir la confirmation que des données vous concernant son traitées et, le cas 
            échéant, d’accéder à vos propres données à caractère personnel et le droit d’en obtenir ou d’en faire une copie 
            dans la mesure du raisonnable.
        </li>
        <li>- Droit de rectification (Art. 16 RGPD) : Vous avez le droit d’obtenir la rectification des données inexactes vous 
            concernant. Vous avez également le droit de compléter les données incomplètes.
        </li>
        <li>- Droit à l’oubli (ou Droit d’effacement - Art. 17 du RGPD) et Droit à la limitation du traitement 
            (Art. 18 du RGPD) : Nous nous engageons à octroyer l’effacement de vos données à caractère personnel notamment 
            dans les cas suivants :
            <ol>- Données n’étant plus nécessaires au regard des finalités pour lesquelles elles avaient été collectées ou traitées ;</ol>
            <ol>- Vous vous opposez au traitement ;</ol>
            <ol>- Les données à caractère personnel ont fait l’objet d’un traitement illicite.</ol>
        </li>
        <li>- Droit de portabilité (Art. 20 RGPD) : Vous avez le droit de recevoir vos données dans un format structuré, couramment 
            utilisé et lisible par une machine, pour votre usage personnel ou pour les transmettre à un tiers de votre choix.
        </li>
        <li>- Droit de retirer le consentement à tout moment (Art.7 du RGPD) : Vous pouvez retirer votre consentement au 
            traitement de vos données lorsque le traitement est basé sur votre consentement. Le retrait du consentement ne 
            compromet pas la licéité du traitement fondé sur le consentement effectué avant ce retrait.
        </li>
        <li>- Droit d’introduire une réclamation (Art. 77 du RGPD) : Le client a le droit d’introduire à tout moment une 
            réclamation auprès de la Commission vie privée s’il estime que le traitement de ses données à caractère personnel 
            constitue une violation du RGPD.
        </li>
    </ul>

