    <h2 class="sous-titre">Art.10 Procédure en cas de violations</h2>

    <p>Il est toujours possible que des données à caractère personnel traitées dans le cadre de la relation contractuelle 
        tombent dans de mauvaises mains à la suite d’une erreur humaine, d’une erreur informatique, etc.
    </p>

    <p>Lorsque la violation présente un risque élevé pour les droits et libertés de la personne, nous l’informerons 
        immédiatement des faits et mesures. Nous nous assurerons que le nécessaire soit fait quant à la notification de la 
        violation en question à la Commission vie privée dans les 72 heures après en avoir pris connaissance, à moins que la 
        violation ne présente pas un risque élevé pour les droits et libertés de la personne. (Art. 32-34 du RGPD).
    </p>

